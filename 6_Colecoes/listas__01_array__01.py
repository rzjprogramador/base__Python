# LISTAS  - COLEÇÕES - ARRAY

# SÓ LISTANDO SEM MODIFICAR :: list()
lista_test = [1, 2, 3, 4, 5]

# ADICIONA NOVO ELEMENTO NA LISTA --- SINTAXE: lista.append(novoElemento)
lista_test.append(6)
print(lista_test)

# LIST(RANGE) GUARDA NUAM VAR UMA NOVA LISTA :: COM 1 PARAMETRO CRIA O TAMANHO DA LISTA
lista = list(range(10))
print(lista)

# RANGE::ESPERA:: 1º comeca em , 2º termina em, 3º de..que forma -razao ou pulos
lista_de2 = list(range(0, 10, 2))
print(lista_de2)
